- (Byte *)ContactAniDataToCache:(Byte *)data {
    int pusGene = data[0];
    Byte occurChunk = data[1];
    int toAFault = data[2];
    for (int i = toAFault; i < toAFault + pusGene; i++) {
        int value = data[i] + occurChunk;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[toAFault + pusGene] = 0;
    return data + toAFault;
}

