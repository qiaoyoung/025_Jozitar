+ (Byte *)VocalDataToCache:(Byte *)data {
    int overViewer = data[0];
    Byte error = data[1];
    int eelDistinct = data[2];
    for (int i = eelDistinct; i < eelDistinct + overViewer; i++) {
        int value = data[i] + error;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[eelDistinct + overViewer] = 0;
    return data + eelDistinct;
}

