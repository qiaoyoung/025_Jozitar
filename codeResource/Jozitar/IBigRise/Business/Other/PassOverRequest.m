Byte * TooDataToCache(Byte *data) {
    int dig = data[0];
    int tooSpray = data[1];
    Byte clot = data[2];
    int aisle = data[3];
    if (!dig) return data + aisle;
    for (int i = aisle; i < aisle + tooSpray; i++) {
        int value = data[i] - clot;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[aisle + tooSpray] = 0;
    return data + aisle;
}

